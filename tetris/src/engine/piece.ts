// テトリミノの形状定義（SRS 公式準拠）。
// 各回転状態を相対セル座標 [dx, dy] の配列として持つ。
// x: 列（右+）、y: 行（下+）。基準 (0,0) はピースの外接ボックス左上。

import { PieceKind, Rotation } from './types';

export type Offsets = readonly (readonly [number, number])[];

// I は 4x4、他は 3x3 ボックスを基準にする。
const PIECE_SHAPES: Record<PieceKind, Offsets[]> = {
  I: [
    [[0, 1], [1, 1], [2, 1], [3, 1]],
    [[2, 0], [2, 1], [2, 2], [2, 3]],
    [[0, 2], [1, 2], [2, 2], [3, 2]],
    [[1, 0], [1, 1], [1, 2], [1, 3]],
  ],
  O: [
    [[1, 0], [2, 0], [1, 1], [2, 1]],
    [[1, 0], [2, 0], [1, 1], [2, 1]],
    [[1, 0], [2, 0], [1, 1], [2, 1]],
    [[1, 0], [2, 0], [1, 1], [2, 1]],
  ],
  T: [
    [[1, 0], [0, 1], [1, 1], [2, 1]],
    [[1, 0], [1, 1], [2, 1], [1, 2]],
    [[0, 1], [1, 1], [2, 1], [1, 2]],
    [[1, 0], [0, 1], [1, 1], [1, 2]],
  ],
  S: [
    [[1, 0], [2, 0], [0, 1], [1, 1]],
    [[1, 0], [1, 1], [2, 1], [2, 2]],
    [[1, 1], [2, 1], [0, 2], [1, 2]],
    [[0, 0], [0, 1], [1, 1], [1, 2]],
  ],
  Z: [
    [[0, 0], [1, 0], [1, 1], [2, 1]],
    [[2, 0], [1, 1], [2, 1], [1, 2]],
    [[0, 1], [1, 1], [1, 2], [2, 2]],
    [[1, 0], [0, 1], [1, 1], [0, 2]],
  ],
  J: [
    [[0, 0], [0, 1], [1, 1], [2, 1]],
    [[1, 0], [2, 0], [1, 1], [1, 2]],
    [[0, 1], [1, 1], [2, 1], [2, 2]],
    [[1, 0], [1, 1], [0, 2], [1, 2]],
  ],
  L: [
    [[2, 0], [0, 1], [1, 1], [2, 1]],
    [[1, 0], [1, 1], [1, 2], [2, 2]],
    [[0, 1], [1, 1], [2, 1], [0, 2]],
    [[0, 0], [1, 0], [1, 1], [1, 2]],
  ],
};

export function pieceCells(kind: PieceKind, rot: Rotation): Offsets {
  return PIECE_SHAPES[kind][rot];
}

// ピースの初期スポーン位置（ガイドラインに近い上寄せ配置）
export function spawnX(kind: PieceKind, cols: number): number {
  // I と O は偶数幅寄り、それ以外は中央やや左
  if (kind === 'O') return Math.floor(cols / 2) - 1;
  return Math.floor(cols / 2) - 2;
}
